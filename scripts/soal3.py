a = 0b10100
b = 0b01001
bitwise_and = a & b
bitwise_or = a | b
bitwise_xor = a ^ b
print("Nilai dari operasi bitwise \"AND\" antara a:%d, b:%d -> (a and b) = %d "%(a,b, bitwise_and))
print("Nilai dari operasi bitwise \"OR\" antara a:%d, b:%d -> (a or b) = %d "%(a,b, bitwise_or))
print("Nilai dari operasi bitwise \"XOR\" antara a:%d, b:%d -> (a xor b) = %d "%(a,b, bitwise_xor))